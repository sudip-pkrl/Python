def python_poem():
    verses = [
        "\n",
        "Python reigns, Neat and sweet",
        "No semicolons, Syntax fleet",
        "Debugging queen, Functions with grace",
        "Libraries vast, Conquer space",
        "Data flows, Logic grows",
        "Jupyter guides, Errors hide",
        "Python's might, Code shines bright",
        "Python's tale, We'll regale",
        "\n"
    ]
    for verse in verses:
        print(verse)
python_poem() 
